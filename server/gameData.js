// ---------- утилиты ----------
export const rnd = (arr) => arr[Math.floor(Math.random() * arr.length)]
export const rndInt = (min, max) => Math.floor(Math.random() * (max - min + 1)) + min
export const clamp = (n) => Math.max(0, Math.min(100, n))
export const uid = () => Math.random().toString(36).slice(2, 9)
export const genCode = () => Math.random().toString(36).slice(2, 6).toUpperCase()

export function makeCards() {
  const prof = rnd(PROFESSIONS)
  return {
    profession: { value: prof.name, icon: prof.icon, bonus: prof.bonus, revealed: false },
    hobby: { value: rnd(HOBBIES), revealed: false },
    phobia: { value: rnd(PHOBIAS), revealed: false },
    trait: { value: rnd(TRAITS), revealed: false },
    bio: { value: rnd(BIO), revealed: false },
    item: { value: rnd(ITEMS), revealed: false },
    baggage: { value: rnd(BAGGAGE), revealed: false },
  }
}

export function makeBunker() {
  const res = {}
  RESOURCE_KEYS.forEach((k) => (res[k] = rndInt(40, 80)))
  const sizes = ["Малый (50 м²)", "Средний (120 м²)", "Большой (300 м²)", "Огромный (500 м²)"]
  const feats = [
    "Автономная вентиляция", "Скрытый запасной выход", "Оранжерея на гидропонике",
    "Оружейный арсенал", "Медицинский отсек", "Спутниковая связь",
    "Артезианская скважина", "Солнечные панели на поверхности",
  ]
  return { size: rnd(sizes), feature: rnd(feats), resources: res }
}

export function applyEffect(resources, effect) {
  const next = { ...resources }
  Object.entries(effect).forEach(([k, v]) => {
    if (next[k] != null) next[k] = clamp(next[k] + v)
  })
  return next
}
